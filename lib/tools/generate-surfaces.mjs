#!/usr/bin/env node
import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const root = process.env.TOOLS_HOME || path.resolve(import.meta.dirname, '../..');
const args = process.argv.slice(2);
const check = args.includes('--check');
const requested = args.find((arg) => !arg.startsWith('-')) || 'all';
const validTargets = new Set(['all', 'completions', 'readme']);

if (!validTargets.has(requested)) {
  console.error(`unknown generation target: ${requested}`);
  process.exit(2);
}

const aggregate = JSON.parse(execFileSync(path.join(root, 'bin/tools'), ['describe'], {
  encoding: 'utf8',
  env: { ...process.env, TOOLS_HOME: root },
}));
const tools = aggregate.tools.filter((tool) => tool.ok).sort((a, b) => a.name.localeCompare(b.name));

function zsh(value) {
  return String(value).replaceAll('\\', '\\\\').replaceAll("'", "'\\''").replaceAll('[', '\\[').replaceAll(']', '\\]');
}

function optionSpec(arg) {
  const flags = arg.flags.join(' ');
  const repeat = arg.repeatable ? '*' : '';
  const label = arg.flags.length > 1 ? `{${arg.flags.join(',')}}` : arg.flags[0];
  let suffix = '';
  if (arg.takes_value) {
    const choices = arg.choices?.length ? `:value:(${arg.choices.map(zsh).join(' ')})` : ':value:';
    suffix = choices;
  }
  return `${repeat}${label}[${zsh(arg.help)}]${suffix}`;
}

function positionalSpec(arg, index) {
  const prefix = arg.variadic ? '*' : `${index}`;
  const choices = arg.choices?.length ? `:(${arg.choices.map(zsh).join(' ')})` : ':';
  return `${prefix}:${zsh(arg.help || arg.name)}${choices}`;
}

function markdownArg(arg) {
  if (arg.positional) {
    const choices = arg.choices?.length ? arg.choices.join('|') : arg.name;
    const token = arg.required ? `<${choices}>` : `[${choices}]`;
    return arg.variadic ? `${token}...` : token;
  }
  const flag = arg.flags?.join(', ') || arg.name;
  if (!arg.takes_value) return flag;
  const value = arg.choices?.length ? arg.choices.join('|') : 'value';
  return `${flag} <${value}>`;
}

function markdownInvocation(tool, command = null) {
  const args = command ? command.args || [] : [
    ...(tool.global_options || []),
    ...(tool.positionals || []),
  ];
  const positional = args.filter((arg) => arg.positional).map(markdownArg);
  return [tool.name, command?.name, ...positional].filter(Boolean).join(' ');
}

function effectLabel(scope) {
  return [scope.effect || 'read', scope.network ? 'network' : '', scope.interactive ? 'interactive' : '']
    .filter(Boolean)
    .join(' + ');
}

function markdownCell(value) {
  return String(value || '').replaceAll('|', '\\|').replaceAll('\n', ' ');
}

function renderReadmeInventory() {
  const directoryDescriptions = new Map([
    ['.github', 'CI workflows and repository automation'],
    ['apps', 'optional macOS Finder integrations'],
    ['bin', 'public executables; one file per tool'],
    ['lib', 'shared helpers and tool-specific implementation'],
    ['config', 'tracked defaults and user-local templates'],
    ['schemas', 'machine-enforced cross-tool contracts'],
    ['completions', 'generated zsh completion'],
    ['tests', 'hermetic Bats coverage'],
    ['bench', 'measured claims asserted in CI'],
    ['docs', 'architecture and contract explanations'],
    ['archive', 'retired scripts kept for reference'],
  ]);
  const tracked = execFileSync(
    'git',
    ['ls-files', '--cached', '--others', '--exclude-standard'],
    { cwd: root, encoding: 'utf8' },
  )
    .trim()
    .split('\n')
    .filter(Boolean);
  const directories = [...new Set(
    tracked.filter((file) => file.includes('/')).map((file) => file.split('/')[0]),
  )].sort();
  const width = Math.max(...tools.map((tool) => tool.name.length), 4);
  const lines = [
    '<!-- BEGIN GENERATED REPO INVENTORY -->',
    '<!-- Generated by `tools generate readme`; do not edit this block. -->',
    '```text',
    'tools/',
    '  bin/                   # public command surface',
  ];
  for (const tool of tools) {
    lines.push(`    ${tool.name.padEnd(width)}  # ${tool.description}`);
  }
  for (const name of directories) {
    if (name === 'bin') continue;
    const description = directoryDescriptions.get(name) || 'tracked repository files';
    lines.push(`  ${(name + '/').padEnd(22)} # ${description}`);
  }
  lines.push('```', '<!-- END GENERATED REPO INVENTORY -->');
  return lines.join('\n');
}

function renderCompletion() {
  const names = tools.map((tool) => tool.name).join(' ');
  const lines = [
    `#compdef ${names}`,
    '# Generated by `tools generate completions`; do not edit.',
    '',
    '_tools_suite_contract() {',
    '  local service="${service:-${words[1]}}"',
    '  local -a commands',
    '  local context state state_descr line',
    '  typeset -A opt_args',
    '  case "$service" in',
  ];

  for (const tool of tools) {
    lines.push(`    ${tool.name})`);
    if (tool.commands.length) {
      lines.push('      commands=(');
      for (const command of tool.commands) {
        lines.push(`        '${zsh(command.name)}:${zsh(command.summary)}'`);
      }
      lines.push('      )');
      const globals = tool.global_options.map((arg) => `'${optionSpec(arg)}'`);
      lines.push('      _arguments -C \\');
      for (const spec of globals) lines.push(`        ${spec} \\`);
      lines.push(`        '(-h --help)'{-h,--help}'[show help]' \\`);
      lines.push(`        '1:command:->command' '*::argument:->args'`);
      lines.push('      case "$state" in');
      lines.push("        command) _describe 'command' commands ;;");
      lines.push('        args)');
      lines.push('          case "${words[2]}" in');
      for (const command of tool.commands) {
        const args = command.args.map((arg, index) => {
          const spec = arg.positional ? positionalSpec(arg, index + 1) : optionSpec(arg);
          return `'${spec}'`;
        });
        if (!args.length) continue;
        lines.push(`            ${command.name}) _arguments ${args.join(' ')} ;;`);
      }
      lines.push('          esac');
      lines.push('          ;;');
      lines.push('      esac');
    } else {
      const specs = [
        ...tool.global_options.map(optionSpec),
        ...tool.positionals.map((arg, index) => positionalSpec(arg, index + 1)),
      ];
      lines.push('      _arguments \\');
      for (const spec of specs) lines.push(`        '${spec}' \\`);
      lines.push(`        '(-h --help)'{-h,--help}'[show help]'`);
    }
    lines.push('      ;;');
  }

  lines.push('  esac', '}', '', '_tools_suite_contract "$@"', '');
  return lines.join('\n');
}

function renderReadmeReference() {
  const lines = [
    '<!-- BEGIN GENERATED CLI REFERENCE -->',
    '<!-- Generated by `tools generate readme`; do not edit this block. Narrative guides continue below. -->',
    '### CLI Reference',
    '',
    'This reference is generated from the same `--describe` contracts as `-h`,',
    'zsh completion, and the TUI. Run `tools generate readme` after changing a',
    'command surface.',
  ];
  for (const tool of tools) {
    lines.push('', `#### \`${tool.name}\``, '', tool.description || '');
    if (tool.commands.length) {
      lines.push('', '| Invocation | Arguments / options | Effect | Summary |', '|---|---|---|---|');
      for (const command of tool.commands) {
        const surface = (command.args || []).map((arg) => `\`${markdownArg(arg)}\``).join('<br>') ||
          (command.delegates ? `Delegated: ${command.delegates}` : '—');
        lines.push(`| \`${markdownInvocation(tool, command)}\` | ${markdownCell(surface)} | \`${effectLabel(command)}\` | ${markdownCell(command.summary)} |`);
      }
    } else {
      lines.push('', `Usage: \`${markdownInvocation(tool)}\``);
      const args = [...tool.global_options, ...tool.positionals];
      if (args.length) {
        lines.push('', '| Argument | Description |', '|---|---|');
        for (const arg of args) {
          lines.push(`| \`${markdownArg(arg)}\` | ${markdownCell(arg.help)} |`);
        }
      }
      lines.push('', `Effect: \`${effectLabel(tool)}\``);
    }
  }
  lines.push('<!-- END GENERATED CLI REFERENCE -->');
  return lines.join('\n');
}

function updateFile(file, content) {
  const current = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : '';
  if (current === content) return true;
  if (check) {
    console.error(`generated file is stale: ${path.relative(root, file)}`);
    return false;
  }
  fs.writeFileSync(file, content);
  console.log(`generated ${path.relative(root, file)}`);
  return true;
}

function replaceGeneratedBlock(content, begin, end, block, fallbackHeading) {
  const pattern = new RegExp(`${begin}[\\s\\S]*?${end}`);
  if (pattern.test(content)) return content.replace(pattern, block);
  return content.replace(fallbackHeading, `${fallbackHeading}\n\n${block}`);
}

function updateReadme(reference, inventory) {
  const file = path.join(root, 'README.md');
  const current = fs.readFileSync(file, 'utf8');
  let next = replaceGeneratedBlock(
    current,
    '<!-- BEGIN GENERATED CLI REFERENCE -->',
    '<!-- END GENERATED CLI REFERENCE -->',
    reference,
    '## Tools',
  );
  next = replaceGeneratedBlock(
    next,
    '<!-- BEGIN GENERATED REPO INVENTORY -->',
    '<!-- END GENERATED REPO INVENTORY -->',
    inventory,
    "## What's in the box",
  );
  return updateFile(file, next);
}

let ok = true;
if (requested === 'all' || requested === 'completions') {
  ok = updateFile(path.join(root, 'completions/_tools-suite'), renderCompletion()) && ok;
}
if (requested === 'all' || requested === 'readme') {
  ok = updateReadme(renderReadmeReference(), renderReadmeInventory()) && ok;
}
if (!ok) process.exit(1);
