#!/usr/bin/env node
import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const root = process.env.TOOLS_HOME || path.resolve(import.meta.dirname, '../..');
const args = process.argv.slice(2);
const check = args.includes('--check');
const requested = args.find((arg) => !arg.startsWith('-')) || 'all';
const validTargets = new Set(['all', 'completions', 'readme']);

if (!validTargets.has(requested)) {
  console.error(`unknown generation target: ${requested}`);
  process.exit(2);
}

const aggregate = JSON.parse(execFileSync(path.join(root, 'bin/tools'), ['describe'], {
  encoding: 'utf8',
  env: { ...process.env, TOOLS_HOME: root },
}));
const tools = aggregate.tools.filter((tool) => tool.ok).sort((a, b) => a.name.localeCompare(b.name));

function zsh(value) {
  return String(value).replaceAll('\\', '\\\\').replaceAll("'", "'\\''").replaceAll('[', '\\[').replaceAll(']', '\\]');
}

function optionSpec(arg) {
  const flags = arg.flags.join(' ');
  const repeat = arg.repeatable ? '*' : '';
  const label = arg.flags.length > 1 ? `{${arg.flags.join(',')}}` : arg.flags[0];
  let suffix = '';
  if (arg.takes_value) {
    const choices = arg.choices?.length ? `:value:(${arg.choices.map(zsh).join(' ')})` : ':value:';
    suffix = choices;
  }
  return `${repeat}${label}[${zsh(arg.help)}]${suffix}`;
}

function positionalSpec(arg, index) {
  const prefix = arg.variadic ? '*' : `${index}`;
  const choices = arg.choices?.length ? `:(${arg.choices.map(zsh).join(' ')})` : ':';
  return `${prefix}:${zsh(arg.help || arg.name)}${choices}`;
}

function renderCompletion() {
  const names = tools.map((tool) => tool.name).join(' ');
  const lines = [
    `#compdef ${names}`,
    '# Generated by `tools generate completions`; do not edit.',
    '',
    '_tools_suite_contract() {',
    '  local service="${service:-${words[1]}}"',
    '  local -a commands',
    '  local context state state_descr line',
    '  typeset -A opt_args',
    '  case "$service" in',
  ];

  for (const tool of tools) {
    lines.push(`    ${tool.name})`);
    if (tool.commands.length) {
      lines.push('      commands=(');
      for (const command of tool.commands) {
        lines.push(`        '${zsh(command.name)}:${zsh(command.summary)}'`);
      }
      lines.push('      )');
      const globals = tool.global_options.map((arg) => `'${optionSpec(arg)}'`);
      lines.push('      _arguments -C \\');
      for (const spec of globals) lines.push(`        ${spec} \\`);
      lines.push(`        '(-h --help)'{-h,--help}'[show help]' \\`);
      lines.push(`        '1:command:->command' '*::argument:->args'`);
      lines.push('      case "$state" in');
      lines.push("        command) _describe 'command' commands ;;");
      lines.push('        args)');
      lines.push('          case "${words[2]}" in');
      for (const command of tool.commands) {
        const args = command.args.map((arg, index) => {
          const spec = arg.positional ? positionalSpec(arg, index + 1) : optionSpec(arg);
          return `'${spec}'`;
        });
        if (!args.length) continue;
        lines.push(`            ${command.name}) _arguments ${args.join(' ')} ;;`);
      }
      lines.push('          esac');
      lines.push('          ;;');
      lines.push('      esac');
    } else {
      const specs = [
        ...tool.global_options.map(optionSpec),
        ...tool.positionals.map((arg, index) => positionalSpec(arg, index + 1)),
      ];
      lines.push('      _arguments \\');
      for (const spec of specs) lines.push(`        '${spec}' \\`);
      lines.push(`        '(-h --help)'{-h,--help}'[show help]'`);
    }
    lines.push('      ;;');
  }

  lines.push('  esac', '}', '', '_tools_suite_contract "$@"', '');
  return lines.join('\n');
}

function renderReadmeIndex() {
  const lines = [
    '<!-- BEGIN GENERATED COMMAND INDEX -->',
    '<!-- Generated by `tools generate readme`; do not edit this block. -->',
    '### Command Index',
    '',
    '| Tool | Command surface |',
    '|---|---|',
  ];
  for (const tool of tools) {
    const commands = tool.commands.length
      ? tool.commands.map((command) => `\`${command.name}\``).join(', ')
      : 'leaf command';
    lines.push(`| \`${tool.name}\` | ${commands} |`);
  }
  lines.push('<!-- END GENERATED COMMAND INDEX -->');
  return lines.join('\n');
}

function updateFile(file, content) {
  const current = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : '';
  if (current === content) return true;
  if (check) {
    console.error(`generated file is stale: ${path.relative(root, file)}`);
    return false;
  }
  fs.writeFileSync(file, content);
  console.log(`generated ${path.relative(root, file)}`);
  return true;
}

function updateReadme(block) {
  const file = path.join(root, 'README.md');
  const current = fs.readFileSync(file, 'utf8');
  const pattern = /<!-- BEGIN GENERATED COMMAND INDEX -->[\s\S]*?<!-- END GENERATED COMMAND INDEX -->/;
  const next = pattern.test(current)
    ? current.replace(pattern, block)
    : current.replace('## Tools\n', `## Tools\n\n${block}\n`);
  return updateFile(file, next);
}

let ok = true;
if (requested === 'all' || requested === 'completions') {
  ok = updateFile(path.join(root, 'completions/_tools-suite'), renderCompletion()) && ok;
}
if (requested === 'all' || requested === 'readme') {
  ok = updateReadme(renderReadmeIndex()) && ok;
}
if (!ok) process.exit(1);
